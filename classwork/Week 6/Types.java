public class Types {
    public static void main(String args[]) {
        float num = 99.999f;
        int num2 = (int) num;
        System.out.println(num2);
        char a = 'a';
        char b = 'b';
        int anum = a;
        int bnum = b;
        System.out.println(anum);
        System.out.println(bnum);
        System.out.println(bnum - anum);
    }
}
